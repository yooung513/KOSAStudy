package sec03;

public class OpTest11 {
	public static void main(String[] args) {
		
		// 축약형 연산 (2)
		int a = 10;
		
		a += 2;		// a = a + 2;
		System.out.println(a);
		
		a -= 2;		// a = a - 2;
		System.out.println(a);
		
		a *= 2;		// a = a * 2;
		System.out.println(a);
		
		a /= 2; 	// a = a / 2;
		System.out.println(a);
		
		a %= 2; 	// a = a % 2;
		System.out.println(a);
		
	}
}
